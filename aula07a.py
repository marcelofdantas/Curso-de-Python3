n1 = int(input('Digite um numero '))
n2 = int(input('Digite outro numero'))
s = n1 + n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
rd = n1 % n2
pot = n1 ** n2
print('Os valores seram os seguintes:\n A soma {}, A multiplicação {}, A divisão {:.3f}\n A divisão inteira {}, O resto da divisão {}, A potência {}'.format(s, m, d, di, rd, pot))

